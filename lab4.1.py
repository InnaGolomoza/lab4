"""Напишіть програму, яка знаходить усі позиції входження підрядка B у рядок A.
На першому рядку міститься вхідний рядок, на другому рядку введення вказаний підрядок,
позиції якого потрібно знайти. Рядки складаються з символів англійського алфавіту.
Програма має вивести позиції входження підрядка B у рядок A (індексація починається з 1),
розділені пропуском або число -1 в разі, коли підрядок не знайдено.
Голомоза Інна Андріївна"""

# Зчитуємо вхідні дані
A = input("Введіть рядок A: ")
B = input("Введіть підрядок B: ")

positions = []

# Цикл по рядку A
for i in range(len(A)):
    match = True
    
    # Перевіряємо, чи співпадає підрядок B з частинкою рядка A
    for j in range(len(B)):
        if i + j >= len(A) or A[i + j] != B[j]:
            match = False
            break
    
    # Якщо підрядок B повністю збігається, зберігаємо позицію
    if match:
        positions.append(i + 1)

# Виводимо результат
if positions:
    print("Позиції входження підрядка B у рядок A:", end=' ')
    for pos in positions:
        print(pos, end=' ')
    print()
else:
    print("Підрядок B не знайдено у рядку A. Результат: -1")
